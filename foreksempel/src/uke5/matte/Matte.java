package uke5.matte;

public class Matte {

	double a, b;

	public double getA() {
		return a;
	}

	public void setA(double a) {
		this.a = a;
	}

	public double getB() {
		return b;
	}

	public void setB(double b) {
		this.b = b;
	}
	
	public double pluss() {
		return a+b;
	}
	
	public double minus() {
		return a-b;
	}
	
	public double gange() {
		return a*b;
	}
	
	public double dele() {
		return a/b;
	}
	
}
